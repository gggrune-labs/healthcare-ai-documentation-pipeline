# ---------------------------------------------------------------------------
# Gemini Client
# ---------------------------------------------------------------------------


class GeminiClient:
    """
    Client for generating structured SOAP notes via Vertex AI Gemini 1.5 Pro.

    Initializes the Vertex AI SDK once on construction. The GenerativeModel
    instance is reused across calls within the same function instance (warm
    start), which avoids re-initializing the SDK on every invocation.
    """

    def __init__(self) -> None:
        vertexai.init(project=GCP_PROJECT, location=GCP_REGION)
        self._model = GenerativeModel(
            model_name=GEMINI_MODEL,
            system_instruction=_SYSTEM_INSTRUCTION,
        )
        logger.info(
            "GeminiClient initialized",
            extra={"model": GEMINI_MODEL, "region": GCP_REGION},
        )

    def generate_soap_note(
        self,
        transcript_text: str,
        session_duration_minutes: int = 50,
        file_id: str = "",
    ) -> SOAPNote:
        """
        Generate a structured SOAP note from a cleaned therapy transcript.

        Builds a multi-turn conversation including few-shot examples, then
        appends the actual transcript as the final user turn. Retries up to
        3 times on transient API errors.

        Args:
            transcript_text: Cleaned plain-text transcript (speaker-labeled).
            session_duration_minutes: Estimated session duration, used as a
                hint in the prompt if the model cannot infer it.
            file_id: Non-PHI Drive file ID, included in log entries for
                traceability.

        Returns:
            SOAPNote dataclass populated from Gemini's JSON response.

        Raises:
            GeminiError: If the API fails after retries or returns invalid JSON.
        """
        prompt_parts = self._build_prompt(transcript_text, session_duration_minutes)

        def _call_api():
            return self._model.generate_content(
                prompt_parts,
                generation_config=_GENERATION_CONFIG,
                safety_settings=_SAFETY_SETTINGS,
            )

        try:
            response = retry_with_backoff(
                _call_api,
                max_attempts=3,
                base_delay=2.0,
                max_delay=20.0,
                logger=logger,
            )
        except Exception as exc:
            raise GeminiError("Gemini API call failed after retries") from exc

        return self._parse_response(response, file_id=file_id)

    def _build_prompt(
        self, transcript_text: str, session_duration_minutes: int
    ) -> list[Part]:
        """
        Construct the full prompt as a list of content Parts.

        Structure:
          1. Few-shot example: user turn (transcript)
          2. Few-shot example: model turn (SOAP JSON)
          3. Actual user turn (transcript to process)
        """
        parts: list[Part] = []

        for example in _FEW_SHOT_EXAMPLES:
            parts.append(
                Part.from_text(
                    f"Transcript:\n{example['transcript'].strip()}"
                )
            )
            parts.append(Part.from_text(example["soap_note"]))

        # Final user turn: the actual transcript
        parts.append(
            Part.from_text(
                f"Transcript (estimated session duration: "
                f"{session_duration_minutes} minutes):\n\n"
                f"{transcript_text.strip()}"
            )
        )
        return parts

    def _parse_response(self, response: Any, file_id: str = "") -> SOAPNote:
        """
        Parse and validate Gemini's response into a SOAPNote.

        Args:
            response: GenerateContentResponse from the Vertex AI SDK.
            file_id: Non-PHI identifier for logging.

        Returns:
            Populated SOAPNote.

        Raises:
            GeminiError: If the response is empty, blocked, or not valid JSON,
                or if required SOAP keys are missing.
        """
        # Check for content filtering or empty response
        if not response.candidates:
            raise GeminiError(
                "Gemini returned no candidates — response may have been blocked"
            )

        candidate = response.candidates[0]
        if not candidate.content or not candidate.content.parts:
            finish_reason = getattr(candidate, "finish_reason", "UNKNOWN")
            raise GeminiError(
                f"Gemini candidate has no content parts. Finish reason: {finish_reason}"
            )

        raw_text = candidate.content.parts[0].text.strip()

        try:
            data = json.loads(raw_text)
        except json.JSONDecodeError as exc:
            raise GeminiError(
                f"Gemini response is not valid JSON: {exc}"
            ) from exc

        missing_keys = _REQUIRED_SOAP_KEYS - set(data.keys())
        if missing_keys:
            raise GeminiError(
                f"Gemini response missing required SOAP keys: {sorted(missing_keys)}"
            )

        # Coerce presenting_concerns to list if the model returned a string
        presenting_concerns = data.get("presenting_concerns", [])
        if isinstance(presenting_concerns, str):
            presenting_concerns = [
                line.lstrip("•-* ").strip()
                for line in presenting_concerns.splitlines()
                if line.strip()
            ]

        logger.info(
            "SOAP note generated successfully",
            extra={
                "file_id": file_id,
                "model": GEMINI_MODEL,
                "presenting_concern_count": len(presenting_concerns),
                "finish_reason": str(
                    getattr(candidate, "finish_reason", "UNKNOWN")
                ),
            },
        )

        return SOAPNote(
            subjective=str(data.get("subjective", "")),
            objective=str(data.get("objective", "")),
            assessment=str(data.get("assessment", "")),
            plan=str(data.get("plan", "")),
            session_duration_minutes=int(data.get("session_duration_minutes", 50)),
            presenting_concerns=presenting_concerns,
            risk_assessment=str(data.get("risk_assessment", "")),
            raw_json=data,
        )
