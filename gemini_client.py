"""
gemini_client.py
----------------
Vertex AI / Gemini 1.5 Pro integration for SOAP note generation.

Responsibilities:
- Build the clinical documentation prompt with system instructions and
  few-shot examples
- Send the cleaned transcript to Gemini 1.5 Pro via the Vertex AI SDK
- Parse and validate the structured JSON response
- Return a typed SOAPNote dataclass
"""

from __future__ import annotations

import json
import os
from dataclasses import dataclass, field
from typing import Any

import vertexai
from vertexai.generative_models import (
    GenerationConfig,
    GenerativeModel,
    HarmBlockThreshold,
    HarmCategory,
    Part,
    SafetySetting,
)

from utils import GeminiError, get_logger, retry_with_backoff

logger = get_logger(__name__)

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

GCP_PROJECT: str = os.environ["GCP_PROJECT"]
GCP_REGION: str = os.environ.get("GCP_REGION", "YOUR_GCP_REGION")
GEMINI_MODEL: str = "gemini-1.5-pro-002"

# Generation parameters tuned for clinical documentation consistency.
_GENERATION_CONFIG = GenerationConfig(
    temperature=0.2,            # Low temperature for deterministic, factual output
    top_p=0.9,
    top_k=40,
    max_output_tokens=4096,
    response_mime_type="application/json",  # Enforce JSON output
)

# Disable safety filters that would incorrectly block medical content.
_SAFETY_SETTINGS: list[SafetySetting] = [
    SafetySetting(category=HarmCategory.HARM_CATEGORY_HARASSMENT,        threshold=HarmBlockThreshold.BLOCK_NONE),
    SafetySetting(category=HarmCategory.HARM_CATEGORY_HATE_SPEECH,       threshold=HarmBlockThreshold.BLOCK_NONE),
    SafetySetting(category=HarmCategory.HARM_CATEGORY_SEXUALLY_EXPLICIT, threshold=HarmBlockThreshold.BLOCK_NONE),
    SafetySetting(category=HarmCategory.HARM_CATEGORY_DANGEROUS_CONTENT, threshold=HarmBlockThreshold.BLOCK_NONE),
]

# Required keys in the Gemini response JSON.
_REQUIRED_SOAP_KEYS: frozenset[str] = frozenset(
    {
        "subjective",
        "objective",
        "assessment",
        "plan",
        "session_duration_minutes",
        "presenting_concerns",
        "risk_assessment",
    }
)

# ---------------------------------------------------------------------------
# Data Classes
# ---------------------------------------------------------------------------


@dataclass
class SOAPNote:
    """
    Structured SOAP note generated by Gemini 1.5 Pro.

    Fields:
        subjective: Patient's self-reported symptoms, concerns, and history
            as described during the session.
        objective: Clinician's observable findings — affect, behavior,
            cognition, appearance.
        assessment: Clinical formulation, diagnostic impressions, progress
            toward treatment goals.
        plan: Next steps — homework, referrals, medication adjustments,
            next session focus.
        session_duration_minutes: Estimated session length extracted from
            transcript timing.
        presenting_concerns: Bullet list of the primary concerns raised
            in the session.
        risk_assessment: Brief statement on any safety concerns (suicidal
            ideation, self-harm, harm to others). "None reported" if absent.
        raw_json: The original JSON dict from Gemini (for auditing).
    """

    subjective: str
    objective: str
    assessment: str
    plan: str
    session_duration_minutes: int
    presenting_concerns: list[str]
    risk_assessment: str
    raw_json: dict[str, Any] = field(default_factory=dict, repr=False)


# ---------------------------------------------------------------------------
# System Instruction
# ---------------------------------------------------------------------------

_SYSTEM_INSTRUCTION = """
You are a licensed clinical documentation assistant helping behavioral health
therapists produce structured SOAP notes from session transcripts.

Your output must be a valid JSON object with exactly these keys:
  - "subjective": string — What the patient reported. Use first-person paraphrase
    (e.g., "Patient reports feeling..."). Do not include the therapist's words.
  - "objective": string — Clinician-observable data: patient's affect, demeanor,
    speech patterns, engagement level, and any notable behavioral observations.
    Infer these from how the patient communicates in the transcript.
  - "assessment": string — Clinical formulation. Summarize the patient's current
    functional status, progress toward stated treatment goals, and any diagnostic
    considerations raised by this session's content.
  - "plan": string — Interventions agreed upon, homework assigned, referrals
    discussed, topics for next session, and any safety planning steps.
  - "session_duration_minutes": integer — Estimated session length in minutes,
    derived from the transcript timestamps or content. Use 50 if unknown.
  - "presenting_concerns": array of strings — 3–7 bullet-point concerns raised
    by the patient during this session (e.g., "Work-related stress", "Sleep
    difficulties", "Relationship conflict").
  - "risk_assessment": string — Any mentions of suicidal ideation, self-harm
    urges, harm to others, or substance use crises. Write "No safety concerns
    reported this session." if none are present. Do not omit this field.

Constraints:
  - Do not invent clinical details not present in the transcript.
  - Do not include the patient's name, date of birth, or other PHI identifiers.
  - Write in professional clinical language appropriate for a medical record.
  - If the transcript is too short or unclear to complete a section, write
    "Insufficient data from this session." for that section.
  - Return ONLY valid JSON. No markdown, no explanation, no code fences.
""".strip()

